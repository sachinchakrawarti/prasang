// Helper function to get poet data based on language
export const getPoetData = (poet, language) => {
  if (!poet) return null;
  
  const lang = language || 'en';
  
  return {
    ...poet,
    name: poet.name?.[lang] || poet.name?.en || poet.name,
    fullName: poet.fullName?.[lang] || poet.fullName?.en || poet.fullName,
    born: poet.born?.[lang] || poet.born?.en || poet.born,
    died: poet.died?.[lang] || poet.died?.en || poet.died,
    birthplace: poet.birthplace?.[lang] || poet.birthplace?.en || poet.birthplace,
    country: poet.country?.[lang] || poet.country?.en || poet.country,
    period: poet.period?.[lang] || poet.period?.en || poet.period,
    language: poet.language?.[lang] || poet.language?.en || poet.language,
    bio: poet.bio?.[lang] || poet.bio?.en || poet.bio,
    longBio: poet.longBio?.[lang] || poet.longBio?.en || poet.longBio,
    famousWorks: poet.famousWorks?.map(work => ({
      ...work,
      title: work.title?.[lang] || work.title?.en || work.title,
      type: work.type?.[lang] || work.type?.en || work.type,
      description: work.description?.[lang] || work.description?.en || work.description
    })),
    famousQuotes: poet.famousQuotes?.map(quote => 
      typeof quote === 'object' ? (quote[lang] || quote.en) : quote
    ),
    writingStyle: poet.writingStyle?.[lang] || poet.writingStyle?.en || poet.writingStyle,
    legacy: poet.legacy?.[lang] || poet.legacy?.en || poet.legacy,
    notableAchievements: poet.notableAchievements?.map(achievement => 
      typeof achievement === 'object' ? (achievement[lang] || achievement.en) : achievement
    )
  };
};