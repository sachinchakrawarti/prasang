// Helper function to get translated poet text
export const translatePoets = (text, language) => {
  const lang = language && poetsTranslations[language] ? language : 'en';
  return poetsTranslations[lang]?.[text] || text;
};

// Hook for using poet translations
export const usePoetsTranslation = (language) => {
  const t = (text) => translatePoets(text, language);
  return { t };
};

